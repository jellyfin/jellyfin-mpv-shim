-- BEGIN patch jellyfin ui
-- While the OSC is visible, keep a low-rate tick alive. Without it,
-- mpv's core loop can sit idle (especially while paused) with mouse
-- events queued: a click's release then gets processed hundreds of ms
-- late, after the pointer state changed, and the button action is lost.
-- The tick's property traffic wakes the core loop so input drains
-- promptly. Killed whenever the OSC is hidden, so idle playback pays
-- nothing.
local jf_keepalive_timer = nil
local function jf_update_keepalive()
    if jf_keepalive_timer == nil then
        jf_keepalive_timer = mp.add_periodic_timer(0.1, function()
            request_tick()
        end)
        jf_keepalive_timer:kill()
    end
    local want = state.enabled and state.osc_visible and not state.idle
    if want and not jf_keepalive_timer:is_enabled() then
        jf_keepalive_timer:resume()
    elseif not want and jf_keepalive_timer:is_enabled() then
        jf_keepalive_timer:kill()
    end
end
-- END patch jellyfin ui

-- called by mpv on every frame
function tick()
    if state.marginsREQ == true then
        update_margins()
        state.marginsREQ = false
    end
    -- BEGIN patch jellyfin ui
    jf_update_keepalive()
    -- END patch jellyfin ui

    if (not state.enabled) then return end

    if (state.idle) then

        -- render idle message
        msg.trace("idle message")
        local _, _, display_aspect = mp.get_osd_size()
        local display_h = 360
        local display_w = display_h * display_aspect
        -- logo is rendered at 2^(6-1) = 32 times resolution with size 1800x1800
        local icon_x, icon_y = (display_w - 1800 / 32) / 2, 140
        local line_prefix = ("{\\rDefault\\an7\\1a&H00&\\bord0\\shad0\\pos(%f,%f)}"):format(icon_x, icon_y)

        local ass = assdraw.ass_new()
        -- mpv logo
        if user_opts.idlescreen then
            for i, line in ipairs(logo_lines) do
                ass:new_event()
                ass:append(line_prefix .. line)
            end
        end

        -- Santa hat
        if is_december and user_opts.idlescreen and not user_opts.greenandgrumpy then
            for i, line in ipairs(santa_hat_lines) do
                ass:new_event()
                ass:append(line_prefix .. line)
            end
        end

        if user_opts.idlescreen then
            ass:new_event()
            ass:pos(display_w / 2, icon_y + 65)
            ass:an(8)
            ass:append("Drop files or URLs to play here.")
        end
        set_osd(display_w, display_h, ass.text)

        if state.showhide_enabled then
            mp.disable_key_bindings("showhide")
            mp.disable_key_bindings("showhide_wc")
            state.showhide_enabled = false
        end


    elseif (state.fullscreen and user_opts.showfullscreen)
        or (not state.fullscreen and user_opts.showwindowed) then

        -- render the OSC
        render()
    else
        -- Flush OSD
        render_wipe()
    end

    state.tick_last_time = mp.get_time()

    if state.anitype ~= nil then
        -- state.anistart can be nil - animation should now start, or it can
        -- be a timestamp when it started. state.idle has no animation.
        if not state.idle and
           (not state.anistart or
            mp.get_time() < 1 + state.anistart + user_opts.fadeduration/1000)
        then
            -- animating or starting, or still within 1s past the deadline
            request_tick()
        else
            kill_animation()
        end
    end
end

function do_enable_keybindings()
    if state.enabled then
        if not state.showhide_enabled then
            -- BEGIN patch jellyfin ui (enable first so input/wc sections
            -- enabled later take priority over the click-to-pause layer)
            mp.enable_key_bindings("jf_click",
                                   "allow-vo-dragging+allow-hide-cursor")
            -- Re-enabling an already-enabled section RAISES it to the
            -- top of mpv's section stack. After an idle/hide cycle this
            -- put jf_click above the still-enabled "input" sections and
            -- every button click fell through to click-to-pause. Force
            -- the render loop to re-enable (= re-raise) them above us.
            state.input_enabled = false
            state.windowcontrols_buttons = false
            -- END patch jellyfin ui
            mp.enable_key_bindings("showhide", "allow-vo-dragging+allow-hide-cursor")
            mp.enable_key_bindings("showhide_wc", "allow-vo-dragging+allow-hide-cursor")
        end
        state.showhide_enabled = true
    end
end

function enable_osc(enable)
    state.enabled = enable
    if enable then
        do_enable_keybindings()
    else
        hide_osc() -- acts immediately when state.enabled == false
        if state.showhide_enabled then
            mp.disable_key_bindings("showhide")
            mp.disable_key_bindings("showhide_wc")
            -- BEGIN patch jellyfin ui
            mp.disable_key_bindings("jf_click")
            -- END patch jellyfin ui
        end
        state.showhide_enabled = false
    end
end

