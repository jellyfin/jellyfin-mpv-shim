-- Draw the jellyfin-web style slider: a thin translucent white track,
-- white-ish buffered ranges, an accent-colored played portion, chapter
-- tick marks (accent once passed) and a round accent thumb that grows
-- on hover. Replaces the static box/nibble drawing entirely.
function render_jf_slider(element, elem_ass)
    local slider_lo = element.layout.slider
    local elem_geo = element.layout.geometry
    local pos = element.slider.posF()
    local seekRanges = element.slider.seekRangesF()
    local cy = elem_geo.h / 2
    local track_h = slider_lo.jf_track_h or 3.4
    local thumb_r = slider_lo.jf_thumb_r or 8
    local x_min = element.slider.min.ele_pos
    local x_max = element.slider.max.ele_pos
    local active = (element.enabled and mouse_hit(element))
        or (elements[state.active_element] == element)

    -- Each part is its own event (each needs its own color/alpha), so
    -- position them absolutely from the hitbox corner with \an7:
    -- alignment-based positioning shifts every event by its own ink box,
    -- which would misalign parts of different heights (thumb vs track).
    local function part(color, alpha)
        elem_ass:new_event()
        elem_ass:pos(element.hitbox.x1, element.hitbox.y1)
        elem_ass:an(7)
        elem_ass:append("{\\blur0\\bord0" .. color .. "\\shad0}")
        ass_append_alpha(elem_ass,
            {[1] = alpha, [2] = 255, [3] = 255, [4] = 255}, 0)
        elem_ass:draw_start()
    end

    -- track background: rgba(255,255,255,0.3)
    part(jf_col.white, 0xB3)
    elem_ass:rect_cw(x_min, cy - track_h / 2, x_max, cy + track_h / 2)
    elem_ass:draw_stop()

    -- buffered/seekable ranges: rgba(255,255,255,0.4)
    if seekRanges then
        part(jf_col.white, 0x99)
        for _, range in pairs(seekRanges) do
            local pstart = get_slider_ele_pos_for(element, range["start"])
            local pend = get_slider_ele_pos_for(element, range["end"])
            elem_ass:rect_cw(pstart, cy - track_h / 2, pend, cy + track_h / 2)
        end
        elem_ass:draw_stop()
    end

    local xp = nil
    if pos then
        xp = get_slider_ele_pos_for(element, pos)
        -- played portion: solid #00a4dc
        part(jf_col.accent, 0)
        elem_ass:rect_cw(x_min, cy - track_h / 2, xp, cy + track_h / 2)
        elem_ass:draw_stop()
    end

    -- chapter tick marks (2x11px): accent when passed, dim white ahead
    if element.slider.markerF then
        local markers = element.slider.markerF()
        for _, watched in ipairs({true, false}) do
            local started = false
            for _, marker in pairs(markers) do
                if marker > element.slider.min.value and
                   marker < element.slider.max.value and
                   ((pos or 0) >= marker) == watched then
                    if not started then
                        if watched then
                            part(jf_col.accent, 0)
                        else
                            part(jf_col.white, 0xB3)
                        end
                        started = true
                    end
                    local s = get_slider_ele_pos_for(element, marker)
                    elem_ass:rect_cw(s - 1, cy - 5.5, s + 1, cy + 5.5)
                end
            end
            if started then
                elem_ass:draw_stop()
            end
        end
    end

    -- round thumb, scaled up while hovered/dragged
    if xp then
        local r = thumb_r * (active and 1.25 or 1.0)
        part(jf_col.accent, 0)
        ass_draw_rr_h_cw(elem_ass, xp - r, cy - r, xp + r, cy + r, r, false)
        elem_ass:draw_stop()
    end
end

-- Hover bubble above the seekbar, modeled on jellyfin-web's slider
-- bubble: a dark #282828 rounded box holding the trickplay thumbnail
-- (drawn by thumbfast.lua via overlay-add through a hole punched in the
-- box) with the chapter name and hovered timestamp underneath.
-- Returns true when a thumbnail was requested.
function render_jf_seek_bubble(element, elem_ass, sliderpos)
    -- No hover previews while an action sheet is open: the thumbnail
    -- overlay would draw over (or fight with) the sheet.
    if jf_sheet.open then
        return false
    end
    local duration = mp.get_property_number("duration", nil)
    if sliderpos == nil then
        return false
    end
    local possec = duration and duration * (sliderpos / 100) or nil
    local timetext = element.slider.tooltipF(sliderpos)
    local ch = possec and get_chapter(possec) or nil
    local chtitle = nil
    if ch and ch.title and ch.title ~= "" then
        chtitle = ch.title:gsub("\\n", " "):gsub("\\$", ""):gsub("{", "\\{")
        if chtitle:len() > 42 then
            chtitle = chtitle:sub(1, 40) .. "..."
        end
    end

    local sx, sy = get_virt_scale_factor()
    local mX = get_virt_mouse_pos()
    local have_thumb = thumbfast.available and possec ~= nil
        and thumbfast.width > 0 and thumbfast.height > 0
    local tw = have_thumb and thumbfast.width * sx or 0
    local th = have_thumb and thumbfast.height * sy or 0

    local pad = 5
    local text_h = chtitle and 40 or 24
    local bw = math.max(tw + pad * 2, 96)
    local bh = th + text_h + pad
    local by2 = element.hitbox.y1 - 10
    local by1 = by2 - bh
    local bx = limit_range(bw / 2 + 4, osc_param.playresx - bw / 2 - 4, mX)
    local x1 = bx - bw / 2

    elem_ass:new_event()
    elem_ass:append(string.format(
        "{\\rDefault\\an7\\pos(0,0)\\blur0\\bord0%s\\shad0}", jf_col.sheet))
    ass_append_alpha(elem_ass, {[1] = 16, [2] = 255, [3] = 255, [4] = 255}, 0)
    elem_ass:draw_start()
    elem_ass:round_rect_cw(x1, by1, x1 + bw, by2, 4)
    if have_thumb then
        -- hole for the overlay-add image, so it shows regardless of
        -- OSD/overlay z-order
        elem_ass:rect_ccw(x1 + pad, by1 + pad, x1 + pad + tw, by1 + pad + th)
    end
    elem_ass:draw_stop()

    if chtitle then
        elem_ass:new_event()
        elem_ass:pos(bx, by2 - pad - 19)
        elem_ass:an(2)
        elem_ass:append(osc_styles.jfSheetAside)
        ass_append_alpha(elem_ass, element.layout.alpha, 0)
        elem_ass:append(chtitle)
    end
    elem_ass:new_event()
    elem_ass:pos(bx, by2 - pad)
    elem_ass:an(2)
    elem_ass:append(osc_styles.jfTooltip)
    ass_append_alpha(elem_ass, element.layout.alpha, 0)
    elem_ass:append(timetext)

    if have_thumb then
        thumbfast_request(
            possec,
            math.floor((x1 + pad) / sx),
            math.floor((by1 + pad) / sy)
        )
        return true
    end
    return false
end
