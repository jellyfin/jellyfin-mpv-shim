-- jellyfin-web behavior: clicking the bare video toggles pause. Defined
-- BEFORE do_enable_keybindings() runs below (enabling an undefined
-- section is a no-op), and enabled before/below the OSC's area-limited
-- "input"/"window-controls" sections so those win over it on the UI.
local jf_click_downX, jf_click_downY = nil, nil

local function jf_video_click()
    -- Judge the click by where the PRESS happened: releases can be
    -- processed late (see the process_event "up" patch), by which time
    -- the pointer state may have moved.
    local mX, mY = jf_click_downX, jf_click_downY
    if mX == nil then
        mX, mY = get_virt_mouse_pos()
    end
    if jf_debug_events then
        msg.info(("jf_click @ %.0f,%.0f"):format(mX, mY))
    end
    -- Never toggle for clicks landing where the UI lives, even while it
    -- is hidden or still fading in: the user was aiming for a control
    -- (the "input"/"window-controls" sections may not have priority yet
    -- mid-fade, which used to make button clicks pause the video).
    for _, name in ipairs({"input", "window-controls"}) do
        for _, cords in ipairs(osc_param.areas[name] or {}) do
            if mX >= cords.x1 and mX <= cords.x2
               and mY >= cords.y1 and mY <= cords.y2 then
                show_osc()
                return
            end
        end
    end
    mp.commandv("cycle", "pause")
end

