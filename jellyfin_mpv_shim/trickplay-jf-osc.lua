local function jf_mpv_track_items(mpv_type, with_off)
    update_tracklist()
    local items = {}
    if with_off then
        table.insert(items, {
            label = tr("off", "Off"),
            selected = get_track(mpv_type) == 0,
            action = function() mp.commandv("set", mpv_type, "no") end,
