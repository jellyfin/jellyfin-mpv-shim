-- A jellyfin-web style "action sheet": an anchored dark popup list with
-- a check mark on the selected row and an optional dimmed current-value
-- label on the right. One instance (the jf_sheet table declared near the
-- top of the file); submenus replace the item list.

local jf_sheet_key_names = {
    "jf-sheet-up", "jf-sheet-down", "jf-sheet-enter", "jf-sheet-esc",
}
local jf_sheet_keys_bound = false

local function jf_sheet_close()
    if not jf_sheet.open then
        return
    end
    jf_sheet.open = false
    jf_sheet.items = {}
    jf_sheet.builder = nil
    jf_sheet.geo = nil
    jf_sheet.hover = nil
    if jf_sheet_keys_bound then
        for _, name in ipairs(jf_sheet_key_names) do
            mp.remove_key_binding(name)
        end
        jf_sheet_keys_bound = false
    end
    request_tick()
end

local function jf_sheet_set_items(items, builder)
    if #items == 0 then
        items = {{label = tr("none", "None"), muted = true}}
    end
    jf_sheet.items = items
    jf_sheet.builder = builder
    jf_sheet.scroll = 0
    jf_sheet.hover = nil
    jf_sheet.geo = nil
end

local function jf_sheet_activate(idx)
    local item = jf_sheet.items[idx]
    if not item or item.muted then
        return
    end
    if item.submenu then
        jf_sheet_set_items(item.submenu(), item.submenu)
    else
        if item.action then
            item.action()
        end
        jf_sheet_close()
    end
    request_tick()
end

local function jf_sheet_move(delta)
    local n = #jf_sheet.items
    if n == 0 then
        return
    end
    local idx = jf_sheet.hover or (delta > 0 and 0 or n + 1)
    for _ = 1, n do
        idx = idx + delta
        if idx < 1 then idx = n end
        if idx > n then idx = 1 end
        if not jf_sheet.items[idx].muted then
            break
        end
    end
    jf_sheet.hover = idx
    -- keep the hovered row scrolled into view
    local g = jf_sheet.geo
    if g then
        if idx - 1 < jf_sheet.scroll then
            jf_sheet.scroll = idx - 1
        elseif idx > jf_sheet.scroll + g.rows_fit then
            jf_sheet.scroll = idx - g.rows_fit
        end
    end
    request_tick()
end

local function jf_sheet_open(builder, anchor, from_top)
    jf_sheet_set_items(builder(), builder)
    jf_sheet.anchor = anchor
    jf_sheet.from_top = from_top or false
    jf_sheet.open = true
    if not jf_sheet_keys_bound then
        mp.add_forced_key_binding("UP", "jf-sheet-up",
            function() jf_sheet_move(-1) end, {repeatable = true})
        mp.add_forced_key_binding("DOWN", "jf-sheet-down",
            function() jf_sheet_move(1) end, {repeatable = true})
        mp.add_forced_key_binding("ENTER", "jf-sheet-enter",
            function()
                if jf_sheet.hover then
                    jf_sheet_activate(jf_sheet.hover)
                end
            end)
        mp.add_forced_key_binding("ESC", "jf-sheet-esc", jf_sheet_close)
        jf_sheet_keys_bound = true
    end
    show_osc()
    request_tick()
end

local function jf_sheet_toggle(builder, anchor, from_top)
    if jf_sheet.open then
        jf_sheet_close()
    else
        jf_sheet_open(builder, anchor, from_top)
    end
end

local function jf_sheet_row_at(mx, my)
    local g = jf_sheet.geo
    if not g or mx < g.x1 or mx > g.x2 or my < g.y1 or my > g.y2 then
        return nil
    end
    local i = math.floor((my - g.y1 - g.top_pad) / g.row_h) + 1
    if i < 1 or i > g.rows_fit then
        return nil
    end
    local idx = i + jf_sheet.scroll
    if idx > #jf_sheet.items then
        return nil
    end
    return idx
end

-- Swallows all OSC input while a sheet is open. Returns true when the
-- event was consumed (i.e. always while open).
local function jf_sheet_process_event(source, what)
    if not jf_sheet.open then
        return false
    end
    local mx, my = get_virt_mouse_pos()
    if source == "mouse_move" then
        state.mouse_in_window = true
        jf_sheet.hover = jf_sheet_row_at(mx, my)
        state.last_mouseX, state.last_mouseY = mx, my
        show_osc()
    elseif source == "mbtn_left" and what == "down" then
        jf_sheet.downX, jf_sheet.downY = mx, my
        local g = jf_sheet.geo
        if not g or mx < g.x1 or mx > g.x2 or my < g.y1 or my > g.y2 then
            jf_sheet_close()
        end
    elseif source == "mbtn_left" and what == "up" then
        -- judge by the press position when the release is processed late
        local idx = jf_sheet_row_at(mx, my)
        if not idx and jf_sheet.downX ~= nil then
            idx = jf_sheet_row_at(jf_sheet.downX, jf_sheet.downY)
        end
        if idx then
            jf_sheet_activate(idx)
        end
    elseif source == "mbtn_right" and what == "down" then
        jf_sheet_close()
    elseif source == "wheel_up" then
        jf_sheet.scroll = math.max(0, jf_sheet.scroll - 1)
    elseif source == "wheel_down" then
        local g = jf_sheet.geo
        local max_scroll = math.max(0, #jf_sheet.items - (g and g.rows_fit or 1))
        jf_sheet.scroll = math.min(max_scroll, jf_sheet.scroll + 1)
    end
    return true
end

local function jf_sheet_render(ass)
    local fs = 16
    local row_h = 27
    local pad_x = 12
    local top_pad = 6
    local check_w = 26
    local items = jf_sheet.items

    -- crude proportional width estimate (bytes x half font size)
    local w = 220
    for _, it in ipairs(items) do
        local tw = #(it.label or "") * fs * 0.55
        if it.aside then
            tw = tw + #it.aside * 14 * 0.55 + 26
        end
        w = math.max(w, tw + check_w + pad_x * 2)
    end
    w = math.min(w, osc_param.playresx * 0.6)

    local n = #items
    local max_h = osc_param.playresy * 0.66
    local rows_fit = math.max(1, math.min(n, math.floor(max_h / row_h)))
    local h = rows_fit * row_h + top_pad * 2

    local anchor_x = (jf_sheet.anchor and state[jf_sheet.anchor])
        or (osc_param.playresx / 2)
    local x1 = limit_range(8, math.max(8, osc_param.playresx - w - 8),
        anchor_x - w / 2)
    local y1, y2
    if jf_sheet.from_top then
        -- dropping down from a top-bar button
        y1 = state.jf_sheet_top or 64
        y2 = y1 + h
    else
        y2 = state.jf_sheet_bottom or (osc_param.playresy - 60)
        y1 = y2 - h
    end

    local max_scroll = math.max(0, n - rows_fit)
    if jf_sheet.scroll > max_scroll then
        jf_sheet.scroll = max_scroll
    end
    jf_sheet.geo = {x1 = x1, y1 = y1, x2 = x1 + w, y2 = y2,
                    row_h = row_h, rows_fit = rows_fit, top_pad = top_pad}

    -- background
    ass:new_event()
    ass:append("{\\rDefault\\an7\\pos(0,0)\\blur0\\bord0" .. jf_col.sheet ..
               "\\shad0\\1a&H14&}")
    ass:draw_start()
    ass:round_rect_cw(x1, y1, x1 + w, y2, 4)
    ass:draw_stop()

    -- hover highlight
    if jf_sheet.hover then
        local vis = jf_sheet.hover - jf_sheet.scroll
        if vis >= 1 and vis <= rows_fit then
            local ry1 = y1 + top_pad + (vis - 1) * row_h
            ass:new_event()
            ass:append("{\\rDefault\\an7\\pos(0,0)\\blur0\\bord0" ..
                       jf_col.white .. "\\shad0\\1a&HE2&}")
            ass:draw_start()
            ass:rect_cw(x1, ry1, x1 + w, ry1 + row_h)
            ass:draw_stop()
        end
    end

    -- rows
    local clip = string.format("{\\clip(%f,%f,%f,%f)}", x1, y1, x1 + w, y2)
    for i = 1, rows_fit do
        local it = items[i + jf_sheet.scroll]
        if not it then
            break
        end
        local ry = y1 + top_pad + (i - 1) * row_h + row_h / 2
        if it.selected then
            ass:new_event()
            ass:pos(x1 + pad_x, ry)
            ass:an(4)
            ass:append(osc_styles.jfSheetText)
            ass:append(icon_ass("check", 15))
        end
        ass:new_event()
        ass:pos(x1 + pad_x + check_w, ry)
        ass:an(4)
        ass:append(osc_styles.jfSheetText)
        if it.muted then
            ass:append("{\\1a&H80&}")
        end
        ass:append(clip)
        ass:append(it.label or "")
        if it.aside then
            ass:new_event()
            ass:pos(x1 + w - pad_x, ry)
            ass:an(6)
            ass:append(osc_styles.jfSheetAside)
            ass:append(clip)
            ass:append(it.aside)
        end
    end

    -- scroll indicator
    if n > rows_fit then
        local track_h = y2 - y1 - 8
        local bar_h = track_h * rows_fit / n
        local bar_y = y1 + 4 + (track_h - bar_h) *
            (jf_sheet.scroll / math.max(1, max_scroll))
        ass:new_event()
        ass:append("{\\rDefault\\an7\\pos(0,0)\\blur0\\bord0" ..
                   jf_col.white .. "\\shad0\\1a&HA0&}")
        ass:draw_start()
        ass:round_rect_cw(x1 + w - 5, bar_y, x1 + w - 2, bar_y + bar_h, 1.5)
        ass:draw_stop()
    end
end

