-- Approximate jellyfin-web's linear-gradient(rgba(16,16,16,0.75) ->
-- transparent). ASS has no real gradients and alpha-stepped bands show
-- visible banding, so instead draw ONE solid translucent box with a
-- heavily blurred edge: the gaussian edge is a continuous alpha ramp.
-- The box extends past the screen on the left/right/bottom so only the
-- top edge fades.
local function add_jf_gradient(prefix, y_top, h, solid_bottom, layer)
    local base_alpha = 56  -- ~0.78 opacity at the solid end
    -- The gaussian edge reaches ~full/zero alpha about 2*blur away from
    -- the box edge, so the edge sits at the fade midpoint.
    local blur = h / 4
    local edge = y_top + h - (h / 2.2)
    local name = prefix .. "box"
    new_element(name, "box")
    local lo = add_layout(name)
    local over = blur * 4
    if solid_bottom then
        -- solid at the bottom, top edge blurred upward across the fade
        lo.geometry = {x = -over, y = edge, an = 7,
                       w = osc_param.playresx + over * 2,
                       h = (y_top + h - edge) + over}
    else
        lo.geometry = {x = -over, y = y_top - over, an = 7,
                       w = osc_param.playresx + over * 2,
                       h = (h / 2.2) + over}
    end
    lo.layer = layer or 2
    lo.style = string.format("{\\blur%.1f\\bord0%s\\shad0}",
                             blur, jf_col.grad)
    lo.alpha[1] = base_alpha
end

layouts["jellyfin"] = function ()
    local playresx = osc_param.playresx
    local playresy = osc_param.playresy

    -- Narrow canvases (playresx is 720 * window aspect, so a tall narrow
    -- window can drop well below 720) shrink the buttons before the
    -- tiered hiding below kicks in.
    local jf_scale = math.min(1, math.max(0.72, playresx / 900))
    local padX = math.floor(18 * jf_scale + 0.5)
    local btn_icon = math.floor(26 * jf_scale + 0.5)
    local btn_box = math.floor(38 * jf_scale + 0.5)
    local btn_gap = math.floor(8 * jf_scale + 0.5)

    local have_pl = mp.get_property_number("playlist-count", 0) > 1
    local have_ch = mp.get_property_number("chapters", 0) > 0
    local duration = mp.get_property_number("duration", 0)

    -- how many audio/subtitle choices exist (shim data when available,
    -- otherwise mpv's own track list): buttons without a real choice to
    -- offer are hidden, like jellyfin-web hides them
    local n_audio, n_subs
    if shim_state and shim_state.has_media then
        n_audio = #(shim_state.audio or {})
        n_subs = math.max(0, #(shim_state.subtitles or {}) - 1) -- minus "Off"
    else
        n_audio = (tracks_osc and tracks_osc.audio and #tracks_osc.audio) or 0
        n_subs = (tracks_osc and tracks_osc.sub and #tracks_osc.sub) or 0
    end
    elements["jf_audio"].visible = n_audio > 1
    elements["jf_sub"].visible = n_subs > 0

    -- responsive hiding, in the spirit of jellyfin-web's breakpoints
    local show_ends = playresx >= 1000
    local show_vol = playresx >= 760
    local show_ch = have_ch and playresx >= 700
    local show_fav = playresx >= 560
    local show_seek_btns = playresx >= 500

    -- top header (back + title + SyncPlay), shifted down when the
    -- borderless window controls occupy the very top of the window
    local top_off = 0
    if window_controls_enabled() then
        top_off = 32 + user_opts.barmargin
    end
    local line_top = top_off + 30
    local top_h = line_top + btn_box / 2 + 6

    local osc_h = 96
    local line_seek = playresy - 58
    local line_btns = playresy - 26

    -- input areas: bottom bar and top header. The OSC shows on any mouse
    -- movement (jellyfin-web behaves the same), so no deadzone here.
    -- (bar_layout resets the areas table; we must too, or every re-init
    -- appends duplicates and the area->section mapping in render() breaks)
    osc_param.areas = {}
    add_area("input", 0, playresy - osc_h, playresx, playresy)
    add_area("input", 0, 0, playresx, top_h)
    add_area("showhide", 0, 0, playresx, playresy)

    add_jf_gradient("jf_grad_b", playresy - 150, 150, true, 2)
    add_jf_gradient("jf_grad_t", 0, 130 + top_off, false, 2)

    local lo

    local function place(name, x_center, y_center)
        local blo = add_layout(name)
        blo.geometry = {x = x_center, y = y_center or line_btns, an = 5,
                        w = btn_box, h = btn_box}
        blo.style = osc_styles.jfButton
        blo.button.icon_size = btn_icon
        return blo
    end

    -- top bar: back, title, SyncPlay
    place("jf_back", padX + btn_box / 2, line_top)
    local title_l = padX + btn_box + 12
    local title_r = playresx - padX
    if elements["jf_syncplay"].visible then
        local sx = playresx - padX - btn_box / 2
        place("jf_syncplay", sx, line_top)
        state.jf_anchor_syncplay = sx
        title_r = sx - btn_box / 2 - 12
    end
    state.jf_sheet_top = line_top + btn_box / 2 + 8

    lo = add_layout("title")
    local tgeo = {x = title_l, y = line_top, an = 4,
                  w = math.max(0, title_r - title_l), h = 24}
    lo.geometry = tgeo
    lo.style = string.format("%s{\\clip(%f,%f,%f,%f)}", osc_styles.jfTitle,
        tgeo.x, tgeo.y - 14, tgeo.x + tgeo.w, tgeo.y + 14)

    -- seek row: position, slider, duration/remaining
    local tcW = (state.tc_ms) and 100 or 70
    if duration >= 3600 then
        tcW = tcW + 16
    end
    lo = add_layout("tc_left")
    lo.geometry = {x = padX, y = line_seek, an = 4, w = tcW, h = 20}
    lo.style = osc_styles.jfTime

    lo = add_layout("tc_right")
    lo.geometry = {x = playresx - padX, y = line_seek, an = 6,
                   w = tcW, h = 20}
    lo.style = osc_styles.jfTime

    local sb_l = padX + tcW + 10
    local sb_r = playresx - padX - tcW - 10
    lo = add_layout("seekbar")
    lo.geometry = {x = sb_l, y = line_seek, an = 4,
                   w = math.max(0, sb_r - sb_l), h = 30}
    lo.style = osc_styles.jfTime
    -- render after the title (higher layer): the hover bubble grows out
    -- of the seekbar's render pass and must draw over the title text
    lo.layer = 60
    lo.slider.stype = "jellyfin"
    lo.slider.jf_track_h = 3.4
    lo.slider.jf_thumb_r = 7
    lo.slider.border = 0
    lo.slider.gap = 0
    lo.slider.tooltip_style = osc_styles.jfTooltip
    lo.slider.tooltip_an = 2

    -- transport cluster (left). Queue prev/next come from the shim's
    -- queue when its state is available; plain-mpv playback falls back
    -- to mpv's own playlist buttons.
    local queue = (shim_state and shim_state.has_media and shim_state.queue)
        or nil
    local left_names = {}
    if queue then
        if queue.has_prev then table.insert(left_names, "jf_prev") end
    elseif have_pl then
        table.insert(left_names, "pl_prev")
    end
    if show_ch then table.insert(left_names, "ch_prev") end
    if show_seek_btns then table.insert(left_names, "skipback") end
    table.insert(left_names, "playpause")
    if show_seek_btns then table.insert(left_names, "skipfrwd") end
    if show_ch then table.insert(left_names, "ch_next") end
    if queue then
        if queue.has_next then table.insert(left_names, "jf_next") end
    elseif have_pl then
        table.insert(left_names, "pl_next")
    end

    local cx = padX + btn_box / 2
    for _, name in ipairs(left_names) do
        place(name, cx)
        cx = cx + btn_box + btn_gap
    end
    elements["playpause"].layout.button.icon_size = btn_icon + 6

    if show_ends and elements["ends_at"].visible then
        lo = add_layout("ends_at")
        lo.geometry = {x = cx - btn_box / 2 + 14, y = line_btns, an = 4,
                       w = 180, h = 20}
        lo.style = osc_styles.jfEndsAt
    end

    -- right cluster: favorite, cc, audio, mute, volume, settings,
    -- fullscreen (matching jellyfin-web's order; absent choices hidden)
    local rx = playresx - padX - btn_box / 2
    place("tog_fs", rx)
    rx = rx - btn_box - btn_gap
    place("jf_settings", rx)
    state.jf_anchor_settings = rx
    rx = rx - btn_box / 2 - btn_gap
    if show_vol then
