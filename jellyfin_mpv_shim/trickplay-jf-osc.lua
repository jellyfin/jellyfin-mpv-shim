-- Render one of the generated vector icons at a given pixel size. The
-- icons are drawn on a 24x24 canvas (with corner anchors pinning the
-- bounding box), so alignment from the element geometry works the same
-- as for text.
local function icon_ass(name, px)
    local path = icons[name]
    if not path then
        return ""
    end
    local scale = px / 24 * 100
    return string.format("{\\fscx%.2f\\fscy%.2f\\p1}%s{\\p0}", scale, scale, path)
end

