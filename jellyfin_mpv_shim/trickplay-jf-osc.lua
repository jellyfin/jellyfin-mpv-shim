function reset_margins()
    if state.using_video_margins then
        for _, opt in ipairs(margins_opts) do
            mp.set_property_number(opt[2], 0.0)
        end
        state.using_video_margins = false
    end
end

function update_margins()
    local margins = osc_param.video_margins

    -- Don't use margins if it's visible only temporarily.
    if (not state.osc_visible) or (get_hidetimeout() >= 0) or
       (state.fullscreen and not user_opts.showfullscreen) or
       (not state.fullscreen and not user_opts.showwindowed)
    then
        margins = {l = 0, r = 0, t = 0, b = 0}
    end

    if user_opts.boxvideo then
        -- check whether any margin option has a non-default value
        local margins_used = false

        if not state.using_video_margins then
            for _, opt in ipairs(margins_opts) do
                if mp.get_property_number(opt[2], 0.0) ~= 0.0 then
                    margins_used = true
                end
            end
        end

        if not margins_used then
            for _, opt in ipairs(margins_opts) do
                local v = margins[opt[1]]
                if (v ~= 0) or state.using_video_margins then
                    mp.set_property_number(opt[2], v)
                    state.using_video_margins = true
                end
            end
        end
    else
        reset_margins()
    end

    mp.set_property("user-data/osc-margins", string.format("%f,%f,%f,%f", margins.l, margins.r, margins.t, margins.b))
end

-- BEGIN patch jellyfin ui
-- While the bottom bar is up, lift bottom-positioned subtitles above it
-- (jellyfin-web does the same). Top-positioned subs (sub-pos < 50) are
-- not obstructed and are left alone. The user's own margin is saved and
-- restored on hide.
local jf_sub_margin = 130
local function update_jf_sub_margin(visible)
    if user_opts.layout ~= "jellyfin" then
        return
    end
    if visible and mp.get_property_number("sub-pos", 100) >= 50 then
        if state.jf_saved_sub_margin == nil then
            state.jf_saved_sub_margin =
                mp.get_property_number("sub-margin-y", 22)
        end
        if state.jf_saved_sub_margin < jf_sub_margin then
            mp.set_property_number("sub-margin-y", jf_sub_margin)
        end
    elseif not visible and state.jf_saved_sub_margin ~= nil then
        mp.set_property_number("sub-margin-y", state.jf_saved_sub_margin)
        state.jf_saved_sub_margin = nil
    end
end
-- END patch jellyfin ui

function shutdown()
