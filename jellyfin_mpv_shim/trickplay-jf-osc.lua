-- BEGIN patch add thumbnails
local thumbfast = {
    width = 0,
    height = 0,
    disabled = true,
    available = false
}

mp.register_script_message("thumbfast-info", function(json)
    local data = utils.parse_json(json)
    if type(data) ~= "table" or not data.width or not data.height then
        msg.error("thumbfast-info: received json didn't produce a table with thumbnail information")
    else
        thumbfast = data
    end
end)

-- Whether a thumb request may be showing (i.e. a "clear" would do anything).
-- The render loop runs on every mouse move; sending "clear" unconditionally
-- spammed the thumbfast script with redundant requests whenever the cursor
-- was away from the seekbar.
local thumb_requested = false
local thumb_last_req = nil

local function thumbfast_request(offset, x, y)
    -- Render runs on every tick (10Hz keepalive while the OSC is shown),
    -- so identical requests must be deduplicated or the thumb message
    -- spams the log/IPC while hovering with playback paused.
    local key = string.format("%.3f:%d:%d", offset, x, y)
    if thumb_requested and key == thumb_last_req then
        return
    end
    thumb_requested = true
    thumb_last_req = key
    mp.commandv("script-message-to", "thumbfast", "thumb", offset, x, y)
end

local function thumbfast_clear()
    if thumb_requested then
        thumb_requested = false
        thumb_last_req = nil
        mp.commandv("script-message-to", "thumbfast", "clear")
    end
end
