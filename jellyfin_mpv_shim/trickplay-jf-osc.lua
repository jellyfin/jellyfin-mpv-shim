end

local function jf_stream_items(kind, verb, mpv_type, with_off)
    if not (shim_state and shim_state[kind]) then
        return jf_mpv_track_items(mpv_type, with_off)
    end
    local items = {}
    for _, s in ipairs(shim_state[kind]) do
        local sid = s.id
        table.insert(items, {
            label = s.label,
            aside = s.aside,
            selected = s.selected,
            action = function() jf_action(verb, tostring(sid)) end,
        })
    end
    return items
end

-- Submenu over an options group ({current, options = {{id, label,
-- selected}, ...}}) living at a dotted path inside shim_state.
local function jf_option_submenu(group_key, verb)
    return function()
        local group = shim_state
        for part in group_key:gmatch("[^%.]+") do
            group = group and group[part]
        end
        local items = {}
        for _, o in ipairs((group and group.options) or {}) do
            local oid = o.id
            table.insert(items, {
                label = o.label,
                selected = o.selected,
                action = function() jf_action(verb, tostring(oid)) end,
            })
        end
        return items
    end
end

